// P: number
// R: string of the number
// E: 7 => 'seven'
// P: use switch case

function switchItUp(number){
  
    switch (number){
      case 0:
        return number = 'zero';
      case 1:
        return number = 'one';
      case 2:
        return number = 'two';
      case 3:
        return number = 'three';
      case 4:
        return number = 'four';
      case 5:
        return number = 'five';
      case 6:
        return number = 'six';
      case 7:
        return number = 'seven';
      case 8:
        return number = 'eight';
      case 9:
        return number = 'nine';
        
    }
  
  }